package org.geosdi.geoplatform.gui.configuration;

/**
 *
 * @author Giuseppe La Scaleia - CNR IMAA geoSDI Group
 * @email giuseppe.lascaleia@geosdi.org
 */
public abstract class GenericTool implements GPGenericTool, Comparable<GenericTool> {

    private static final long serialVersionUID = -379951057942018866L;
    //
    protected String id;
    protected boolean enabled;
    protected int order;
    protected boolean secure = true;

    /**
     * @return the id
     */
    @Override
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the enabled
     */
    @Override
    public boolean isEnabled() {
        return enabled;
    }

    /**
     * @param enabled the enabled to set
     */
    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    /**
     * @return the order
     */
    public int getOrder() {
        return order;
    }

    /**
     * @param order the order to set
     */
    public void setOrder(int order) {
        this.order = order;
    }

    /**
     * @return the secure
     */
    @Override
    public boolean isSecure() {
        return secure;
    }

    /**
     * <pre>
     * Determines whether a tool is under safety. Default to true.
     * </pre>
     *
     * @param secure the secure to set
     */
    public void setSecure(boolean secure) {
        this.secure = secure;
    }

    /**
     * (non-Javadoc)
     *
     * @see java.lang.Comparable#compareTo(java.lang.Object)
     */
    @Override
    public int compareTo(GenericTool o) {
        return getOrder() - o.getOrder();
    }

}
